# String methods

x = input()
print(x.strip()) # Removes all the leading and white spaces from a string
print(len(x)) # Returns the length of a string or a list

# .lower() - Everything in lower case
# .upper() - Everything in upper case

print(x.split(".")) # Creates a list out of the string that you give it - "." Every item separated by this becomes it's own item in the list